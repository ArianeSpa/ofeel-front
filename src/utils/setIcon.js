export default (tag) => {
    if(tag==='sport'){
        return 'football ball';
    }else if (tag==='recuperation'){
        return 'heartbeat';
    }else if (tag==='alimentation'){
        return 'food';
    }else if (tag==='sante'){
        return 'book';
    }else if (tag==='divers'){
        return 'boxes';
    }
};