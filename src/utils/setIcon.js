// eslint-disable-next-line consistent-return
export default (tag) => {
  if (tag === 'sport') {
    return 'football ball';
  }
  if (tag === 'recuperation') {
    return 'bed';
  }
  if (tag === 'alimentation') {
    return 'food';
  }
  if (tag === 'sante') {
    return 'heartbeat';
  }
  if (tag === 'divers') {
    return 'boxes';
  }
};
